// Java allows to add together strings, using the + operator. The result is
// the combination of the strings.
// For example, let's combine the first and last names to result in the full name of a user:

public class strings {
    public static void main(String[] arg){
        String firstname = "Aaliyah";
        String lastname = "Jardien";
        String fullname = firstname+" "+lastname;
        // the empty quotes creates a space between the words
        System.out.println(fullname);
    }
}
// The process of adding strings together is called concatenation.

