// The modulo % (or remainder) math operation returns the remainder of a division. 
// For example, let's use it to find how many of the given items will be left over
// if we place them in boxes of 5:

class modulo {
    public static void main(String[] arg){
        int items = 23;
        int res = items % 5;
        System.out.println(res);
    }
}
